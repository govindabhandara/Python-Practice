def three_digits(num):
    if num>=100 and num<=999:
        print("three digits number")
    else:
        print("not three digits number")

def even_or_odd(num):
    if num%2==0:
        print("even")
    else:
        print("odd")
three_digits(234)
even_or_odd(345)

