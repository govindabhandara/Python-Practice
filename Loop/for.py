#for loop in list
fruits=["apple","banana","cherry"]
for fruit in fruits:
    print(fruit) 


#for loop in string
for char in 'hello':
    print(char)


# for loop in range
for i in range(5):
    print(i)

for i in range(2,6):
    print(i)

for i in range(0,10,2):
    print(i) 

for i in range (0,10,3):
    print(i)