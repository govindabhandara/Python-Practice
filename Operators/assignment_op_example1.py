# Use +=, -=, *=, and /= to update a variable a = 5.
a = 5

# Addition assignment (+=)
a += 3  # Equivalent to: a = a + 3
print(a)  # Output: 8

# Subtraction assignment (-=)
a -= 2  # Equivalent to: a = a - 2
print(a)  # Output: 6

# Multiplication assignment (*=)
a *= 4  # Equivalent to: a = a * 4
print(a)  # Output: 24

# Division assignment (/=)
a /= 3  # Equivalent to:
print(a)