#calculate the sum, difference, product and quotient of two numbers.

a=int(input("enter a"))
b=int(input("enter b"))
#sum
sum=a+b
print(f"the sum of a & b is {sum}.")
#difference
difference =a-b
print(f"the difference of a & b is {difference}")
#product
product=a*b
print(f"the product of a & b is {product}.")
#quotient
quotient=a/b
print(f"the quotient of a & b is {quotient}")